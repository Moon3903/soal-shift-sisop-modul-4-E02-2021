char dirpath[100] = "/home/ryan/Downloads";

void combinePath(const char* a, char* b, char* c){
    if(strcmp(a,"/")==0){
        a = b;
        sprintf(c,"%s",a);
    }else{
        sprintf(c,"%s%s",b,a);
    }
}

static int xmp_getattr(const char *path, struct stat *stbuf)
{
	int res;
    char fpath[100]={0};
    combinePath(path,dirpath,fpath);

	res = lstat(fpath, stbuf);
	if (res == -1)
		return -errno;

	return 0;
}

